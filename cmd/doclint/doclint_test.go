func TestRepositoryIsFullyDocumented(t *testing.T) {
	t.Parallel()

	violations, err := collectViolations(filepath.Join("..", ".."))
	if err != nil {
		t.Fatalf("collectViolations() error = %v, want nil", err)
	}
	for _, v := range violations {
		t.Errorf("%s:%d: %s is missing a doc comment", v.file, v.line, v.name)
	}
}
