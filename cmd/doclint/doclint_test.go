package main

import (
	"os"
	"path/filepath"
	"testing"
)

func write(t *testing.T, dir, name, body string) {
	t.Helper()
	if err := os.WriteFile(filepath.Join(dir, name), []byte(body), 0o644); err != nil {
		t.Fatalf("writing %s: %v", name, err)
	}
}

func TestCollectViolationsFindsUndocumentedFunctions(t *testing.T) {
	t.Parallel()

	root := t.TempDir()
	write(t, root, "documented.go", "package x\n\n// Foo greets.\nfunc Foo() {}\n\n// bar helps.\nfunc bar() {}\n")
	write(t, root, "undocumented.go", "package x\n\nfunc Bare() {}\n\ntype T struct{}\n\nfunc (T) Method() {}\n")
	write(t, root, "generated.go", "// Code generated by hand. DO NOT EDIT.\n\npackage x\n\nfunc Gen() {}\n")
	write(t, root, "helpers_test.go", "package x\n\nfunc Helper() {}\n")

	violations, err := collectViolations(root)

	if err != nil {
		t.Fatalf("collectViolations() error = %v, want nil", err)
	}
	names := make([]string, 0, len(violations))
	for _, v := range violations {
		names = append(names, v.name)
	}
	want := map[string]bool{"Bare": true, "Method": true}
	if len(names) != len(want) {
		t.Fatalf("violations = %v, want exactly the undocumented production funcs %v", names, want)
	}
	for _, name := range names {
		if !want[name] {
			t.Errorf("unexpected violation %q; want only %v", name, want)
		}
	}
}

func TestRunReportsAndFailsOnViolations(t *testing.T) {
	t.Parallel()

	root := t.TempDir()
	write(t, root, "bad.go", "package x\n\nfunc Undocumented() {}\n")

	if err := run(root, os.Stderr); err == nil {
		t.Fatal("run() error = nil, want a failure listing the violation")
	}
}

