package main

import (
	"os"
	"path/filepath"
	"testing"
)

const whatsappManifest = `{
	"id": "whatsapp",
	"name": "WhatsApp",
	"backend": "github.com/gopherium/alphone/plugins/whatsapp",
	"frontend": "@alphone/plugin-whatsapp"
}`

func writePlugin(t *testing.T, root, dir, manifestJSON string) {
	t.Helper()
	pluginDir := filepath.Join(root, "plugins", dir)
	if err := os.MkdirAll(pluginDir, 0o755); err != nil {
		t.Fatalf("creating %s: %v", pluginDir, err)
	}
	if manifestJSON == "" {
		return
	}
	if err := os.WriteFile(filepath.Join(pluginDir, "plugin.json"), []byte(manifestJSON), 0o644); err != nil {
		t.Fatalf("writing manifest: %v", err)
	}
}

func TestLoadManifestsReadsValidPlugins(t *testing.T) {
	t.Parallel()

	root := t.TempDir()
	writePlugin(t, root, "sms-relay", `{"id": "sms-relay", "name": "SMS Relay", "backend": "example.com/sms"}`)
	writePlugin(t, root, "whatsapp", whatsappManifest)
	if err := os.WriteFile(filepath.Join(root, "plugins", "README.md"), []byte("not a plugin"), 0o644); err != nil {
		t.Fatalf("writing stray file: %v", err)
	}

	manifests, err := loadManifests(filepath.Join(root, "plugins"))

	if err != nil {
		t.Fatalf("loadManifests() error = %v, want nil", err)
	}
	if len(manifests) != 2 {
		t.Fatalf("manifests = %d, want 2", len(manifests))
	}
	if manifests[0].ID != "sms-relay" || manifests[1].ID != "whatsapp" {
		t.Errorf("order = [%q, %q], want directory order [%q, %q]",
			manifests[0].ID, manifests[1].ID, "sms-relay", "whatsapp")
	}
	if manifests[1].Backend != "github.com/gopherium/alphone/plugins/whatsapp" {
		t.Errorf("backend = %q, want the module import path", manifests[1].Backend)
	}
	if manifests[1].Frontend != "@alphone/plugin-whatsapp" {
		t.Errorf("frontend = %q, want the npm package name", manifests[1].Frontend)
	}
}

func TestLoadManifestsRejectsInvalidPlugins(t *testing.T) {
	t.Parallel()

	tests := map[string]struct {
		dir      string
		manifest string
	}{
		"missing manifest":             {dir: "ghost", manifest: ""},
		"malformed json":               {dir: "broken", manifest: `{`},
		"missing id":                   {dir: "anon", manifest: `{"name": "Anon", "backend": "example.com/anon"}`},
		"uppercase id":                 {dir: "Loud", manifest: `{"id": "Loud", "name": "Loud", "backend": "example.com/loud"}`},
		"id and directory disagree":    {dir: "alias", manifest: `{"id": "other", "name": "Alias", "backend": "example.com/alias"}`},
		"missing name":                 {dir: "nameless", manifest: `{"id": "nameless", "backend": "example.com/nameless"}`},
		"neither backend nor frontend": {dir: "hollow", manifest: `{"id": "hollow", "name": "Hollow"}`},
	}

	for testName, tc := range tests {
		t.Run(testName, func(t *testing.T) {
			t.Parallel()

			root := t.TempDir()
			writePlugin(t, root, tc.dir, tc.manifest)

			if _, err := loadManifests(filepath.Join(root, "plugins")); err == nil {
				t.Fatal("loadManifests() error = nil, want a validation error")
			}
		})
	}
}

func TestGenerateGoWiresBackendPlugins(t *testing.T) {
	t.Parallel()

	got := string(generateGo([]manifest{
		{ID: "sms-relay", Name: "SMS Relay", Backend: "example.com/sms"},
		{ID: "whatsapp", Name: "WhatsApp", Backend: "github.com/gopherium/alphone/plugins/whatsapp", Frontend: "@alphone/plugin-whatsapp"},
	}))

	want := `// SPDX-License-Identifier: AGPL-3.0-or-later

// Code generated by pluginwire. DO NOT EDIT.

package main

import (
	sms_relay "example.com/sms"
	whatsapp "github.com/gopherium/alphone/plugins/whatsapp"

	"github.com/gopherium/alphone/sdk"
)

func registerPlugins(deps sdk.Deps) ([]sdk.Plugin, error) {
	plugins := make([]sdk.Plugin, 0, 2)
	sms_relayPlugin, err := sms_relay.Register(deps)
	if err != nil {
		return nil, err
	}
	plugins = append(plugins, sms_relayPlugin)
	whatsappPlugin, err := whatsapp.Register(deps)
	if err != nil {
		return nil, err
	}
	plugins = append(plugins, whatsappPlugin)
	return plugins, nil
}
`
	if got != want {
		t.Errorf("generateGo() = %q, want %q", got, want)
	}
}

