func TestRunWritesWiringFiles(t *testing.T) {
	t.Parallel()

	root := t.TempDir()
	writePlugin(t, root, "whatsapp", whatsappManifest)
	for _, dir := range []string{"cmd/alphone", "frontend/src/plugins"} {
		if err := os.MkdirAll(filepath.Join(root, dir), 0o755); err != nil {
			t.Fatalf("creating %s: %v", dir, err)
		}
	}

	if err := run(root); err != nil {
		t.Fatalf("run() error = %v, want nil", err)
	}

	goSrc, err := os.ReadFile(filepath.Join(root, "cmd", "alphone", "plugins_gen.go"))
	if err != nil {
		t.Fatalf("reading generated Go wiring: %v", err)
	}
	tsSrc, err := os.ReadFile(filepath.Join(root, "frontend", "src", "plugins", "index.ts"))
	if err != nil {
		t.Fatalf("reading generated TS wiring: %v", err)
	}
	manifests, err := loadManifests(filepath.Join(root, "plugins"))
	if err != nil {
		t.Fatalf("loadManifests() error = %v, want nil", err)
	}
	if string(goSrc) != string(generateGo(manifests)) {
		t.Errorf("plugins_gen.go = %q, want the generated wiring", goSrc)
	}
	if string(tsSrc) != string(generateTS(manifests)) {
		t.Errorf("index.ts = %q, want the generated registry", tsSrc)
	}
}

func TestRunReportsFailures(t *testing.T) {
	t.Parallel()

	tests := map[string]struct {
		prepare func(t *testing.T, root string)
	}{
		"missing plugins directory": {
			prepare: func(_ *testing.T, _ string) {},
		},
		"unwritable go wiring": {
			prepare: func(t *testing.T, root string) {
				writePlugin(t, root, "whatsapp", whatsappManifest)
			},
		},
		"unwritable ts wiring": {
			prepare: func(t *testing.T, root string) {
				writePlugin(t, root, "whatsapp", whatsappManifest)
				if err := os.MkdirAll(filepath.Join(root, "cmd", "alphone"), 0o755); err != nil {
					t.Fatalf("creating cmd/alphone: %v", err)
				}
			},
		},
	}

	for testName, tc := range tests {
		t.Run(testName, func(t *testing.T) {
			t.Parallel()

			root := t.TempDir()
			tc.prepare(t, root)

			if err := run(root); err == nil {
				t.Fatal("run() error = nil, want an error")
			}
		})
	}
}

func TestRepositoryWiringIsUpToDate(t *testing.T) {
	t.Parallel()

	manifests, err := loadManifests(filepath.Join("..", "..", "plugins"))
	if err != nil {
		t.Fatalf("loadManifests() error = %v, want nil", err)
	}

	goSrc, err := os.ReadFile(filepath.Join("..", "..", "cmd", "alphone", "plugins_gen.go"))
	if err != nil {
		t.Fatalf("reading committed Go wiring: %v", err)
	}
	tsSrc, err := os.ReadFile(filepath.Join("..", "..", "frontend", "src", "plugins", "index.ts"))
	if err != nil {
		t.Fatalf("reading committed TS wiring: %v", err)
	}

	if string(goSrc) != string(generateGo(manifests)) {
		t.Error("cmd/alphone/plugins_gen.go is stale; run make generate")
	}
	if string(tsSrc) != string(generateTS(manifests)) {
		t.Error("frontend/src/plugins/index.ts is stale; run make generate")
	}
}
